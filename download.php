<h2 id="install">README</h2>
<p>There is no installation program. Just unzip the client somewhere and run the binary called john.exe (or john in the Linux/Mac OS X releases) and you're up and running. The node should pop up instantly on your <a href="nodes">Nodes</a> page.</p>


<h2 id="source">Source code</h2>
<p>The client software is a modified version of <a href="http://www.openwall.com/john/" target="_blank">John the Ripper</a>, developed by Solar Designer. It contains patches for additional supported hashes. See the John the Ripper page for more information.</p>
